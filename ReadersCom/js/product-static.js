  function bindCoverHoverZoom(root) {
    var wrap = root.querySelector(".product-cover-zoom");
    if (!wrap) return;

    if (
      typeof window.matchMedia === "function" &&
      window.matchMedia("(prefers-reduced-motion: reduce)").matches
    ) {
      return;
    }

    if (
      typeof window.matchMedia === "function" &&
      window.matchMedia("(hover: none), (pointer: coarse)").matches
    ) {
      return;
    }

    wrap.classList.add("product-cover-zoom--interactive");

    function onMove(e) {
      var rect = wrap.getBoundingClientRect();
      if (rect.width < 1 || rect.height < 1) return;
      var x = ((e.clientX - rect.left) / rect.width) * 100;
      var y = ((e.clientY - rect.top) / rect.height) * 100;
      wrap.style.setProperty("--zoom-x", Math.max(0, Math.min(100, x)) + "%");
      wrap.style.setProperty("--zoom-y", Math.max(0, Math.min(100, y)) + "%");
    }

    wrap.addEventListener("mousemove", onMove);
  }

